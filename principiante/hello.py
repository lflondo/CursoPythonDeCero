# Tipos de datos
print(type("Hola Mundo")) #str
print(type(1)) #int
print(type(1.0)) #float
print(type(1j)) #complex
print(type(b"Hola Mundo")) #bytes
print(type(bytearray(5))) #bytearray
print(type(memoryview(bytes(5)))) #memoryview
print(type(range(5))) #range
print(type({1, 2, 3})) #set
print(type(frozenset({1, 2, 3}))) #frozenset
print(type({1: "uno", 2: "dos", 3: "tres"})) #dict
print(type(True)) #bool
print(type(None)) #NoneType
print(type([])) #list
print(type({})) #dict
print(type(())) #tuple

